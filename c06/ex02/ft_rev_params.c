#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	s;

	s = argc -1;
	while (s > 0)
	{	
		i = 0;
		while (argv[s][i])
		{
			write (1, &argv[s][i], 1);
			i++;
		}
		write (1, "\n", 1);
		s--;
	}
	return (0);
}
