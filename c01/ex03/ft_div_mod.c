#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int main(void)
{
	int x = 11;
	int	y = 5;
	ft_div_mod(x, y, &x, &y);
	printf("%d %d\n", x, y);
}
